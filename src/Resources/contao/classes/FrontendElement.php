namespace Contao;


/**
 * Parent class for front end objects.
 *
 * @author Leo Feyer <https://github.com/leofeyer>
 */
abstract class FrontendElement extends \Frontend
{

	/**
	 * Template
	 * @var string
	 */
	protected $strTemplate;

	/**
	 * Column
	 * @var string
	 */
	protected $strColumn;

	/**
	 * Model
	 * @var \Model
	 */
	protected $objModel;

	/**
	 * Current record
	 * @var array
	 */
	protected $arrData = array();

	/**
	 * Style array
	 * @var array
	 */
	protected $arrStyle = array();


	/**
	 * Compile the current element
	 */
	abstract protected function compile();
}
